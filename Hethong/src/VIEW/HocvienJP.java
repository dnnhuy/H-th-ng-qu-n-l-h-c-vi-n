    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaHV.getText().equals(""))
        {
            sb.append("MaHV đang trống!\n");
            txtMaHV.requestFocus();
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Cảnh báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            Hocvien hv = new Hocvien();
            HOCVIENDAO hvd = new HOCVIENDAO();

            hv.setMaHV(txtMaHV.getText());
            hv.setHoTenHV((txtHotenHV.getText()));
            hv.setNgaySinhHV(txtNgaySinhHV.getText());
            String n = cbbGioiTinhHV.getItemAt(cbbGioiTinhHV.getSelectedIndex());
            hv.setGioiTinhHV(n);

            hvd.insert(hv);
            Loadulieu();
            JOptionPane.showMessageDialog(this, "Thêm học viên thành công!");

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this, "Không thể thêm học viên!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

