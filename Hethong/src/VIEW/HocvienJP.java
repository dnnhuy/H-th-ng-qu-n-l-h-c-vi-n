     public void Loadulieu() {
        try {
            tbl_hv.removeAll();
            String[] arr = {"Mã HV", "Họ tên HV", "Ngày sinh","Giới tính"};
            tbtModel = new DefaultTableModel(arr, 0);
            String sql = "SELECT * FROM HocVien";
            Connection conn = Connect.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            tbtModel.setRowCount(0);

            while (rs.next()) {
                String[] row = new String[]
            {
                    rs.getString("MaHV"), 
                    rs.getString("HoTenHV"),
                    rs.getString("NgaySinhHV"),
                    rs.getString("GioiTinhHV")};
                
                tbtModel.addRow(row);
            }
            tbl_hv.setModel(tbtModel);
            tbl_hv.setAutoCreateRowSorter(true);
            tbl_hv.getTableHeader().setFont(new Font("Arial", Font.BOLD, 18));
            tbl_hv.getTableHeader().setPreferredSize(new Dimension(100, 50));
            tbtModel.fireTableDataChanged();
            Connect.CloseConnect(conn);
            
             rowSorter = new TableRowSorter<>(tbl_hv.getModel());
            tbl_hv.setRowSorter(rowSorter);
            
            jtfSearch.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = jtfSearch.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                String text = jtfSearch.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
            }
        });
            
        } catch (Exception e) {
           e.printStackTrace();
        }
    }
